import { CREATE_DX, CREATE_DX_ERR } from '../actionTypes/dxType'
import { createDxApi } from 'api/Api';
import axios from "axios";
import { accessToken } from 'api/Token'
// GET POSTS ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
export const createDx = payload => async dispatch => {
    try {

        console.log("Payload*******", payload)
        let results = await axios.post(`${createDxApi}`, payload, await accessToken())
        // console.log("createBookingApi", results)
        let data = results && results.data
        dispatch({
            type: CREATE_DX,
            payload: data || [],
        });
    } catch (error) {
        dispatch({
            type: CREATE_DX_ERR,
            payload: error.message
        });
    }
}